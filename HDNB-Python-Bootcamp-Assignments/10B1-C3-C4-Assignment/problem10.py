# Problem 10: Data Structure Summary

# 1. List
my_list = [10, 20, 10]
print("List:", my_list)
# List is ordered, mutable, and allows duplicates.

# 2. Dictionary
my_dict = {'name': 'Alice', 'age': 25}
print("Dictionary:", my_dict)
# Dictionary is ordered, mutable, keys must be unique, values can be duplicated.

# 3. Tuple
my_tuple = (1, 2, 1)
print("Tuple:", my_tuple)
# Tuple is ordered, immutable, and allows duplicates.

# 4. Set
my_set = {1, 2, 2, 3}
print("Set:", my_set)
# Set is unordered, mutable, and does NOT allow duplicates.

