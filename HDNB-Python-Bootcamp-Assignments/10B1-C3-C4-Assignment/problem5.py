# Problem 5: List Operations

fruits = ["apple", "banana", "mango", "grape", "avocado"]

# Adding a new fruit to the list
fruits.append("orange")

# Replace the fruit at the second position
fruits[1] = "kiwi"

# Removing a fruit from the list
fruits.remove("grape")

# Bonus: Print the final list and its length using len().
print("Final list of fruits:", fruits)
print(f"Number of fruits in the list: {len(fruits)}")


