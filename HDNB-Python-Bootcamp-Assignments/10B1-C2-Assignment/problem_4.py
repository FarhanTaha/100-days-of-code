number_int = int(input("Enter a Integer Number: "))

number_str = str(number_int) # converting to string
print(f'String: "{number_str}"')

number_float = float(number_int) # converting to float
print(f'Float: {number_float}')

number_boolean = bool(number_int) # converting to boolean
print(f'Boolean: {number_boolean}')

